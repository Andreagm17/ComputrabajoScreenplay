package com.sophos.certificacion.searchcomputrabajo.model;

public class SearchModel {
	
	private String area;
	private String place;
	
	public SearchModel(String area, String place) {
		this.area = area;
		this.place = place;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}
	
	
	
	

}
